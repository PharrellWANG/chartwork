#include <chartwork/plugins/HistogramPlugin.h>

#include <QtPlugin>

#include <chartwork/Histogram.h>

////////////////////////////////////////////////////////////////////////////////////////////////////
//
// HistogramPlugin
//
////////////////////////////////////////////////////////////////////////////////////////////////////

HistogramPlugin::HistogramPlugin(QObject *parent)
:	QObject(parent)
{
	m_isInitialized = false;
}

////////////////////////////////////////////////////////////////////////////////////////////////////

void HistogramPlugin::initialize(QDesignerFormEditorInterface *)
{
	if (m_isInitialized)
		return;

	m_isInitialized = true;
}

////////////////////////////////////////////////////////////////////////////////////////////////////

bool HistogramPlugin::isInitialized() const
{
	return m_isInitialized;
}

////////////////////////////////////////////////////////////////////////////////////////////////////

QWidget *HistogramPlugin::createWidget(QWidget *parent)
{
	return new chartwork::Histogram(parent);
}

////////////////////////////////////////////////////////////////////////////////////////////////////

QString HistogramPlugin::name() const
{
	return "chartwork::Histogram";
}

////////////////////////////////////////////////////////////////////////////////////////////////////

QString HistogramPlugin::group() const
{
	return "Chartwork";
}

////////////////////////////////////////////////////////////////////////////////////////////////////

QIcon HistogramPlugin::icon() const
{
	return QIcon("://icons/icon-histogram.png");
}

////////////////////////////////////////////////////////////////////////////////////////////////////

QString HistogramPlugin::toolTip() const
{
	return "";
}

////////////////////////////////////////////////////////////////////////////////////////////////////

QString HistogramPlugin::whatsThis() const
{
	return "Histogram";
}

////////////////////////////////////////////////////////////////////////////////////////////////////

bool HistogramPlugin::isContainer() const
{
	return false;
}

////////////////////////////////////////////////////////////////////////////////////////////////////

QString HistogramPlugin::domXml() const
{
	return
		("<ui language=\"c++\" displayname=\"Histogram\">\
			<widget class=\"chartwork::Histogram\" name=\"histogram\">\
				<property name=\"geometry\">\
					<rect>\
						<x>0</x>\
						<y>0</y>\
						<width>400</width>\
						<height>300</height>\
					</rect>\
				</property>\
			</widget>\
		</ui>");
}

////////////////////////////////////////////////////////////////////////////////////////////////////

QString	HistogramPlugin::includeFile() const
{
	return QLatin1String("<chartwork/Histogram.h>");
}
