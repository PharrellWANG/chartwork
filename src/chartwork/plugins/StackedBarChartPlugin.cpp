#include <chartwork/plugins/StackedBarChartPlugin.h>

#include <QtPlugin>

#include <chartwork/StackedBarChart.h>

////////////////////////////////////////////////////////////////////////////////////////////////////
//
// StackedBarChartPlugin
//
////////////////////////////////////////////////////////////////////////////////////////////////////

StackedBarChartPlugin::StackedBarChartPlugin(QObject *parent)
:	QObject(parent)
{
	m_isInitialized = false;
}

////////////////////////////////////////////////////////////////////////////////////////////////////

void StackedBarChartPlugin::initialize(QDesignerFormEditorInterface *)
{
	if (m_isInitialized)
		return;

	m_isInitialized = true;
}

////////////////////////////////////////////////////////////////////////////////////////////////////

bool StackedBarChartPlugin::isInitialized() const
{
	return m_isInitialized;
}

////////////////////////////////////////////////////////////////////////////////////////////////////

QWidget *StackedBarChartPlugin::createWidget(QWidget *parent)
{
	return new chartwork::StackedBarChart(parent);
}

////////////////////////////////////////////////////////////////////////////////////////////////////

QString StackedBarChartPlugin::name() const
{
	return "chartwork::StackedBarChart";
}

////////////////////////////////////////////////////////////////////////////////////////////////////

QString StackedBarChartPlugin::group() const
{
	return "Chartwork";
}

////////////////////////////////////////////////////////////////////////////////////////////////////

QIcon StackedBarChartPlugin::icon() const
{
	return QIcon("://icons/icon-stacked-bar-chart.png");
}

////////////////////////////////////////////////////////////////////////////////////////////////////

QString StackedBarChartPlugin::toolTip() const
{
	return "";
}

////////////////////////////////////////////////////////////////////////////////////////////////////

QString StackedBarChartPlugin::whatsThis() const
{
	return "Stacked Bar Chart";
}

////////////////////////////////////////////////////////////////////////////////////////////////////

bool StackedBarChartPlugin::isContainer() const
{
	return false;
}

////////////////////////////////////////////////////////////////////////////////////////////////////

QString StackedBarChartPlugin::domXml() const
{
	return
		("<ui language=\"c++\" displayname=\"StackedBarChart\">\
			<widget class=\"chartwork::StackedBarChart\" name=\"stackedBarChart\">\
				<property name=\"geometry\">\
					<rect>\
						<x>0</x>\
						<y>0</y>\
						<width>400</width>\
						<height>300</height>\
					</rect>\
				</property>\
			</widget>\
		</ui>");
}

////////////////////////////////////////////////////////////////////////////////////////////////////

QString	StackedBarChartPlugin::includeFile() const
{
	return QLatin1String("<chartwork/StackedBarChart.h>");
}
