#include <chartwork/plugins/PieChartPlugin.h>

#include <QtPlugin>

#include <chartwork/PieChart.h>

////////////////////////////////////////////////////////////////////////////////////////////////////
//
// PieChartPlugin
//
////////////////////////////////////////////////////////////////////////////////////////////////////

PieChartPlugin::PieChartPlugin(QObject *parent)
:	QObject(parent)
{
	m_isInitialized = false;
}

////////////////////////////////////////////////////////////////////////////////////////////////////

void PieChartPlugin::initialize(QDesignerFormEditorInterface *)
{
	if (m_isInitialized)
		return;

	m_isInitialized = true;
}

////////////////////////////////////////////////////////////////////////////////////////////////////

bool PieChartPlugin::isInitialized() const
{
	return m_isInitialized;
}

////////////////////////////////////////////////////////////////////////////////////////////////////

QWidget *PieChartPlugin::createWidget(QWidget *parent)
{
	return new chartwork::PieChart(parent);
}

////////////////////////////////////////////////////////////////////////////////////////////////////

QString PieChartPlugin::name() const
{
	return "chartwork::PieChart";
}

////////////////////////////////////////////////////////////////////////////////////////////////////

QString PieChartPlugin::group() const
{
	return "Chartwork";
}

////////////////////////////////////////////////////////////////////////////////////////////////////

QIcon PieChartPlugin::icon() const
{
	return QIcon("://icons/icon-pie-chart.png");
}

////////////////////////////////////////////////////////////////////////////////////////////////////

QString PieChartPlugin::toolTip() const
{
	return "";
}

////////////////////////////////////////////////////////////////////////////////////////////////////

QString PieChartPlugin::whatsThis() const
{
	return "Pie Chart";
}

////////////////////////////////////////////////////////////////////////////////////////////////////

bool PieChartPlugin::isContainer() const
{
	return false;
}

////////////////////////////////////////////////////////////////////////////////////////////////////

QString PieChartPlugin::domXml() const
{
	return
		("<ui language=\"c++\" displayname=\"PieChart\">\
			<widget class=\"chartwork::PieChart\" name=\"pieChart\">\
				<property name=\"geometry\">\
					<rect>\
						<x>0</x>\
						<y>0</y>\
						<width>400</width>\
						<height>300</height>\
					</rect>\
				</property>\
			</widget>\
		</ui>");
}

////////////////////////////////////////////////////////////////////////////////////////////////////

QString	PieChartPlugin::includeFile() const
{
	return QLatin1String("<chartwork/PieChart.h>");
}
