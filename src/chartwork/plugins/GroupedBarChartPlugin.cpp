#include <chartwork/plugins/GroupedBarChartPlugin.h>

#include <QtPlugin>

#include <chartwork/GroupedBarChart.h>

////////////////////////////////////////////////////////////////////////////////////////////////////
//
// GroupedBarChartPlugin
//
////////////////////////////////////////////////////////////////////////////////////////////////////

GroupedBarChartPlugin::GroupedBarChartPlugin(QObject *parent)
:	QObject(parent)
{
	m_isInitialized = false;
}

////////////////////////////////////////////////////////////////////////////////////////////////////

void GroupedBarChartPlugin::initialize(QDesignerFormEditorInterface *)
{
	if (m_isInitialized)
		return;

	m_isInitialized = true;
}

////////////////////////////////////////////////////////////////////////////////////////////////////

bool GroupedBarChartPlugin::isInitialized() const
{
	return m_isInitialized;
}

////////////////////////////////////////////////////////////////////////////////////////////////////

QWidget *GroupedBarChartPlugin::createWidget(QWidget *parent)
{
	return new chartwork::GroupedBarChart(parent);
}

////////////////////////////////////////////////////////////////////////////////////////////////////

QString GroupedBarChartPlugin::name() const
{
	return "chartwork::GroupedBarChart";
}

////////////////////////////////////////////////////////////////////////////////////////////////////

QString GroupedBarChartPlugin::group() const
{
	return "Chartwork";
}

////////////////////////////////////////////////////////////////////////////////////////////////////

QIcon GroupedBarChartPlugin::icon() const
{
	return QIcon("://icons/icon-grouped-bar-chart.png");
}

////////////////////////////////////////////////////////////////////////////////////////////////////

QString GroupedBarChartPlugin::toolTip() const
{
	return "";
}

////////////////////////////////////////////////////////////////////////////////////////////////////

QString GroupedBarChartPlugin::whatsThis() const
{
	return "Grouped Bar Chart";
}

////////////////////////////////////////////////////////////////////////////////////////////////////

bool GroupedBarChartPlugin::isContainer() const
{
	return false;
}

////////////////////////////////////////////////////////////////////////////////////////////////////

QString GroupedBarChartPlugin::domXml() const
{
	return
		("<ui language=\"c++\" displayname=\"GroupedBarChart\">\
			<widget class=\"chartwork::GroupedBarChart\" name=\"groupedBarChart\">\
				<property name=\"geometry\">\
					<rect>\
						<x>0</x>\
						<y>0</y>\
						<width>400</width>\
						<height>300</height>\
					</rect>\
				</property>\
			</widget>\
		</ui>");
}

////////////////////////////////////////////////////////////////////////////////////////////////////

QString	GroupedBarChartPlugin::includeFile() const
{
	return QLatin1String("<chartwork/GroupedBarChart.h>");
}
